		<h1>Le projet Thalie</h1>
		<div class="project">
			<p>Thalie est un logiciel de <strong>suivi des troubles cognitifs</strong> à destination des neuropsychologues et des acteurs de soins.</p>          
			<p>Standardisé et guidé par la voix, c'est un outil qui permet un suivi optimisé des individus présentant des troubles cognitifs.</br>
			Des fonctionnalités telles que la <strong>cotation automatique</strong> ou bien la <strong>reconnaissance vocale</strong> (lors du test T5M par exemple) facilitent la réalisation des tests cognitifs habituels.</p>

			<p>Cet outil a pour objectif de reproduire des tests neuropsychologiques classiques existant actuellement sous format papier (test de Zazzo, reproduction d’un dessin, fluence verbale, etc.).
			Il consiste en un logiciel se décomposant selon <strong>deux interfaces</strong> : </p>
			<p>- l’une destinée aux neuropsychologues cliniciens, accessible via un écran</p>
			<img id="horloge" src="images/horloge.PNG" alt="test de l'horloge"/>
			<p>- l’autre destinée aux personnes âgées, accessible via une tablette tactile. </p>
		</div>

		<p>Le projet consistait à évaluer son <strong>utilisabilité</strong> et fournir un <strong>manuel utilisateur</strong> à destination des neuropsychologues.</p>
		
